        public MainWindow()
        {
            InitializeComponent();
        }

        private void OnbtnOpenfileClicked(object sender, RoutedEventArgs e)
        {
            OpenFileDialog openFileDialog = new OpenFileDialog();
            openFileDialog.Multiselect = true;
            openFileDialog.Filter = "Text files (*.txt)|*.txt|All files (*.*)|*.*";
            openFileDialog.InitialDirectory = @"D:\openSourceProject\GUI\WPF\HelloWPF";
            if (openFileDialog.ShowDialog() == true)
            {
                foreach(var filename in openFileDialog.FileNames)
                {
                    ltbFiles.Items.Add(System.IO.Path.GetFileName(filename));
                }
            }
        }
    }
